#!/usr/bin/python
# -*- coding: UTF-8 -*-

str = "wwW.runooB.cOm"

if __name__ == '__main__':
    print(str.upper())  # 把所有字符转换成大写        WWW.RUNOOB.COM
    print(str.lower())  # 把所有字符转换成小写        www.runoob.com
    print(str.capitalize())  # 把第一个字母转换成大写，其余转成小写   Www.runoob.com
    print(str.title())  # 把每个字符的第一个字符转成大写       Www.Runoob.Com
